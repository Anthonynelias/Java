
public class TestaVariaveis {
	
	public static void main(String[] args) {
		
		System.out.println("Teste de Variáveis");
		
		int idadeNova;		
		idadeNova  = 20; //Declaração de variável
		
		int idadeVelha = 55; //Mesma forma de declarar variável com valor
		
		System.out.println("Idade nova = " + idadeNova);
		System.out.println("Idade velha = " + idadeVelha);
		System.out.println(idadeNova + " + " + idadeVelha + " = " + (idadeNova + idadeVelha));

	}
}
